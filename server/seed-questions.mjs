for (let i = 0; i < 100; i++) {
  const q = arabicQuestions[i];
  const qType = q.type || "اختيار من متعدد";
  const opts = q.opts ? JSON.stringify(q.opts) : '[]';
  const r = await c.query(
    `INSERT INTO questions (subject_id, question_text, options, correct_answer, question_type, difficulty) VALUES ($1, $2, $3::jsonb, $4, $5, $6) RETURNING id`,
    [subjectId, q.q, opts, q.ans, qType, q.diff]
  );
  qIds.push(r.rows[0].id);
  if (i < 12) {
    await c.query(
      `INSERT INTO exam_questions (exam_id, question_id, order_number, points) VALUES ($1, $2, $3, 1) ON CONFLICT DO NOTHING`,
      [examId, r.rows[0].id, i]
    );
  }
}

